/**
 * Boolean adalah suatu tipe data yang hanya mempunyai dua nilai. Yaitu true atau false (benar atau salah).
 */

let umurTaufik = 22

if (umurTaufik == 22) {
    umurTaufik = true
} else {
    umurTaufik = false
}

console.log(umurTaufik)



// set perbandingan == hanya membandingkan nilai 
var test1 = 9 == '9'
console.log(test1)


// set perbandingan identik ===
var test2 = 9 === '9'
console.log(test2)

var test3 = 8 != 9
console.log(test3)

var test4 = 8 !== '8'
console.log(test4) 

